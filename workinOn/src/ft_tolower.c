#include "libft.h"

int	ft_tolower(int c)
{
	if ((c <= 'A' && c >= 'Z') || (c <= 'a' && c >= 'z'))
		return (c);
	if (c >= 'a' && c <= 'z')
		return (c);
	if (c >= 'A' && c <= 'Z')
		c = c + 32;
	return (c);
}
